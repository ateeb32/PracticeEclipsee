
public class Paint {
	
	public double size;
	public double price;
	public double coverage;
	
	public Paint(double size, double price, double coverage) {
		
		this.size = size;
		this.price = price;
		this.coverage = coverage;
		
	}

	public double getSize() {
		return size;
	}

	public double getPrice() {
		return price;
	}

	public double getCoverage() {
		return coverage;
	}

}
